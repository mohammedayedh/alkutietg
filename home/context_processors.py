def translate(text, language='ar'):
    """ترجمة النص حسب اللغة"""
    return TRANSLATIONS.get(language, {}).get(text, text)

def translations(request):
    """إضافة دالة الترجمة للـ context"""
    # قراءة اللغة من الجلسة أو استخدام العربية كافتراضي
    language = request.session.get('django_language', 'ar')
    
    # حفظ اللغة في request لاستخدامها في القوالب
    request.LANGUAGE_CODE = language
    
    def trans(text):
        return translate(text, language)
    
    return {
        'trans': trans,
        'LANGUAGE_CODE': language,
    }
